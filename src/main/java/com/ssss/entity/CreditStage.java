package com.ssss.entity;

public class CreditStage {
	private Integer creditID;
	private Integer stageID;
	private Float stageCharge;
	/**
	 * @return the creditID
	 */
	public Integer getCreditID() {
		return creditID;
	}
	/**
	 * @param creditID the creditID to set
	 */
	public void setCreditID(Integer creditID) {
		this.creditID = creditID;
	}
	/**
	 * @return the stageID
	 */
	public Integer getStageID() {
		return stageID;
	}
	/**
	 * @param stageID the stageID to set
	 */
	public void setStageID(Integer stageID) {
		this.stageID = stageID;
	}
	/**
	 * @return the stageCharge
	 */
	public Float getStageCharge() {
		return stageCharge;
	}
	/**
	 * @param stageCharge the stageCharge to set
	 */
	public void setStageCharge(Float stageCharge) {
		this.stageCharge = stageCharge;
	}
}
